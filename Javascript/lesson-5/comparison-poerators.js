// Comparison Operators
let a = 5;
let b = 10;
let c = "5";
console.log(a === b); // Output: false
console.log(a === c); // Output: false
console.log(a == b); // Output: false
console.log(a == c); // Output: true
console.log(a != b); // Output: true
console.log(a < b); // Output: true
console.log(a > b); // Output: false
console.log(a <= b); // Output: true
console.log(a >= b); // Output: false
